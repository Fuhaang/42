char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (str[i] > 64 && str[i] < 91)
			str[i] = str[i] + 32;
		i++;
	}
	if (str[0] > 96 && str[0] < 123)
		str[0] = str[0] - 32;
	i = 1;
	while (str[i])
	{
		if (str[i] > 96 && str[i] < 123)
		{
			if (!((str[i - 1] > 64 && str[i - 1] < 91)
				|| (str[i - 1] > 96 && str[i - 1] < 123)
				|| (str[i - 1] > 47 && str[i - 1] < 58)))
				str[i] = str[i] - 32;
		}
		i++;
	}
	return (str);
}
